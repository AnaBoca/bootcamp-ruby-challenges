class Image
  def initialize(array)
    @image_data = array
  end

  def output_image
    @image_data.each do |row|
      puts row.join
    end
  end

  def blur(distance = 1)
    cell_array =[]

    distance.times do

      @image_data.each_with_index do |row, row_index|
        row.each_with_index do |column, column_index|
          cell_array << [row_index, column_index] if column == 1
        end
      end

      cell_array.each do |cords|
        # Up
        @image_data[cords.first - 1][cords.last] = 1 if cords.first > 0
        
        # Down
        @image_data[cords.first + 1][cords.last] = 1 if cords.first < @image_data.length - 1

        # Right
        @image_data[cords.first][cords.last + 1] = 1 if cords.last < @image_data.first.length - 1

        # left
        @image_data[cords.first][cords.last - 1] = 1 if cords.last > 0
      end
    end  
  end
end

